public class Switch {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("s");
                break;
            case 2:
                System.out.println("rohith");
                break;
            case 3:
                System.out.println("h");
                break;
            default:
                System.out.println("invalid");
        }
    }
}
